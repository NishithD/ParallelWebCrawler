package com.udacity.webcrawler.profiler;

import javax.inject.Inject;
import java.io.*;
import java.lang.reflect.Proxy;
import java.nio.file.Path;
import java.time.Clock;
import java.time.ZonedDateTime;
import java.util.Objects;

import static java.time.format.DateTimeFormatter.RFC_1123_DATE_TIME;

/**
 * Concrete implementation of the {@link Profiler}.
 */
final class ProfilerImpl implements Profiler {

    private final Clock clock;
    private final ProfilingState state = new ProfilingState();
    private final ZonedDateTime startTime;

    @Inject
    ProfilerImpl(Clock clock) {
        this.clock = Objects.requireNonNull(clock);
        this.startTime = ZonedDateTime.now(clock);
    }

    @Override
    @SuppressWarnings("unchecked")
    public <T> T wrap(Class<T> klass, T delegate) {
        Objects.requireNonNull(klass);
        return (T) Proxy.newProxyInstance(klass.getClassLoader(),
                new Class[]{delegate.getClass()},
                new ProfilingMethodInterceptor(clock));
    }

    @Override
    public void writeData(Path path) {
        Objects.requireNonNull(path);
        File ps = new File(String.valueOf(path));
        boolean fileExist = ps.exists();
        try (BufferedWriter bufferedWriter = new BufferedWriter(new FileWriter(String.valueOf(path), fileExist))) {
            writeData(bufferedWriter);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    @Override
    public void writeData(Writer writer) throws IOException {
        writer.write("Run at " + RFC_1123_DATE_TIME.format(startTime));
        writer.write(System.lineSeparator());
        state.write(writer);
        writer.write(System.lineSeparator());
    }
}
